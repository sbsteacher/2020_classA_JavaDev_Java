package com.koreait.first.loop;

public class For {

	public static void main(String[] args) {
		for(int i=9; i>4; i--) {
			System.out.println("Hi~ ");
		}
		
		System.out.println("---------------");
		
		// 50 ~ 100찍는 반복문 작성 해보세요.
		
		for(int i=50; i<=100; i++) {
			System.out.println(i-50);
		}
		
	}


}