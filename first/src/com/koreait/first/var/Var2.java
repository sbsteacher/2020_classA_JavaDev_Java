package com.koreait.first.var;

public class Var2 {
	public static void main(String[] args) {
		int n1; //변수 선언, 즉 변수 선언을 하셔야 변수를 사용할 수 있습니다!!!!
		//변수란 의미가 값을 바꿀 수 있다고 해서 변수
		
		// int n1; //선언은 두번 하면 안 된다.
		
		n1 = 20; //초기화
		System.out.println(n1);
		n1 = 22; //값을 할당
		
		int n2 = 10; //변수 선언과 동시에 초기화
		
		n2 = n1;
		
		System.out.println(n1);
		System.out.println(n2);
		
	}
}




