package com.koreait.first.var;

//변수타입 설명
public class Var1 {
	public static void main(String[] args) {
		//Primitive 타입 (원시형 타입) / 일반변수타입 라고 하기도 합니다.
		//정수형, 실수형, 불린형, 문자형이 있다.
		
		//정수형
		byte d1; //1byte (8bit) -128~127 | 00000000
		short d2; //2byte |  0000000000000000
		int d3; //4byte (32bit)
		long d4; //8byte
		
		//실수형
		float f1; //4byte
		double f2; //8byte
		
		//불린형
		boolean b1; // true or false
		
		//문자형
		char c1; 
		
		
		
		//주석 > 프로그래밍에서 실행을 하지 않는 부분!
		//즉, 우리가 코멘트를 남기고 싶을 때
		//혹은 프로그램이 실행되지 않았으면 할 때 사용할 수 있음.
		
		// > 싱글라인 주석
		
		/*		  
		  멀티라인 주석		  
		*/ 
	}
}
